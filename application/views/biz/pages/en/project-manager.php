                                                            <p class="sub-title">These are following elements of project
                                                                management:</p>
                                                            <ul class="crm-text">
                                                                <li>Initiating: The first stage of PM, where finalize of
                                                                    the project plan, charter and scope are defined so
                                                                    as to determine the cost, resources, timeline, and
                                                                    budgeting for the project.</li>
                                                                <li>Planning: The most crucial stage of all, this stage
                                                                    includes strategist scope of a project, identifying
                                                                    risks if any and developing easing plans by creating
