                                                            <div
                                                                class="col-lg-12  col-md-12 col-md-offset-1 col-sm-12 col-xs-12">
                                                                <p class="crm-text"> A project manager is the individual tasked with
                                                                    planning and executing the project. This is the
                                                                    person responsible for leading the team and
                                                                    organizing the work. In more formal, structured
                                                                    organizations and on more complex projects, the
                                                                    project manager is usually a certified project
                                                                    management professional (PMP) by the Project
                                                                    Management Institute (PMI). In more informal
                                                                    organizations, such as a small business, the project
                                                                    manager does not require certification. Project
                                                                    managers are responsible for all the different
                                                                    project management processes taking place throughout
                                                                    the project life cycle, such as risk management,
                                                                    task management, and resource management. In simple
                                                                    terms, they supervise the planning, execution,
                                                                    monitoring and closure of the project. However, most
                                                                    project managers share common roles and
                                                                    responsibilities. Some of the more traditional
                                                                    duties of a project manager include the following:
                                                                </p>
                                                                <ul class="crm-text">
                                                                    <li>Scope Management: Defining the work needed to
                                                                        complete the project activities.</li>
                                                                    <li>Task Management: Planning tasks and defining
                                                                        their deliverables.</li>
                                                                    <li>Resource Management: Using people, capital,
                                                                        materials and all other resources efficiently.
                                                                    </li>
                                                                    <li>Team Management: Assembling and leading a team.
                                                                    </li>
                                                                    <li>Schedule Management: Analyzing the duration of
                                                                        activities to create a project schedule. Once
                                                                        the execution phase begins, the project status
                                                                        must be monitored to update the schedule
                                                                        baseline.</li>
                                                                    <li>Quality Management: Establishing a quality
                                                                        policy for the project’s deliverables and
                                                                        implementing quality assurance and quality
                                                                        control procedures</li>
                                                                    <li>Cost Management: Estimating costs and creating a
                                                                        budget.</li>
                                                                    <li>Stakeholder Management: Satisfying stakeholders
                                                                        expectations and communicating with them
                                                                        throughout the project life cycle.</li>
                                                                    <li>Risk management: Identifying, monitoring and
                                                                        minimizing project risk.</li>
                                                                    <li>Status Reporting: Monitoring and tracking
                                                                        progress and performance by generating reports
                                                                        and other documentatio.</li>
                                                                </ul>
                                                                <p class="crm-text">Project managers learn and receive certification from
                                                                    the Project Management Institute (PMI), which has
                                                                    certified standards in the Project Management Book
                                                                    of Knowledge (PMBOK). The Project Management
                                                                    Professional certification (PMP) is standard for
                                                                    project managers, but the PMI offers more
                                                                    specialized training choices such as the program
                                                                    management professional (PgMP) and Portfolio
                                                                    Management Professional (PfMP). Armed with their
                                                                    knowledge and skills, project managers rely on
                                                                    project management software to execute all of the
                                                                    tasks necessary for a successful project..</p>
