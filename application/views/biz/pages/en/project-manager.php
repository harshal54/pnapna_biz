                                                                    <h4 class=" heading-title" style="font-size: 35px">
                                                                        Types of Project Management</h4>
                                                                </div>
                                                            </div>
                                                        </div>
                                                    </div>

                                                    <div class="row  ">
                                                        <div class="container">

                                                            <div class="row   ">

                                                                <div class="col-lg-12  col-md-12  col-sm-12 col-xs-12">

                                                                    <ul class="crm-text">
                                                                        <li>Waterfall Project Management: This is
                                                                            similar to traditional project management
                                                                            but includes the caution that each task
                                                                            needs to be completed before the next one
                                                                            starts. Steps are linear and progress flows
                                                                            in one direction, like a waterfall. Because
                                                                            of this, attention to task and timelines are
                                                                            very important in this type of project
                                                                            management. Often, the size of the team
                                                                            working on the project will grow as smaller
                                                                            tasks are completed and larger tasks begin.
                                                                        </li>
                                                                        <li>Agile Project Management: The computer
                                                                            software industry was the first one to use
                                                                            this methodology. With the basis originating
                                                                            in the 12 core principles of the Agile
                                                                            platform, agile project management is a
                                                                            constant process focused on the continuous
                                                                            monitoring and improvement of product. At
                                                                            its core, high-quality output is a result of
                                                                            providing customer value, team interactions,
                                                                            and accepting to current business
                                                                            circumstances.Agile project management does
                                                                            not follow a sequential stage-by-stage
                                                                            approach. Instead, the project is completed
                                                                            in parallel to each other by various team
                                                                            members in an organization. This approach
                                                                            can find and correct errors without having
                                                                            to restart the entire procedure.</li>
                                                                        <li>Lean Project Management: This methodology is
                                                                            all about avoiding waste, both time and
                                                                            resources. The principles of this
                                                                            methodology were extracting from Japanese
                                                                            manufacturing practices. The main idea
                                                                            behind them is to create more value for
                                                                            customers with fewer resources.</li>
                                                                        <li>Scrum Project Management: Scrum is the
                                                                            abstract of agile. It's fast, very small in
                                                                            scope, and able to turn on a quarters. Scrum
                                                                            is all about rush to accomplish projects in
                                                                            small pieces, often based on a one-month
                                                                            timeline. Scrum is great for smaller teams
                                                                            that are looking to repeat quickly.</li>
                                                                        <li>Kanban Project Management: Kanban is another
                                                                            variant of agile project management. Unlike
                                                                            Scrum, which is focused on time-based
                                                                            pieces, Kanban is all about organisation. To
                                                                            accomplish this, Kanban looks primarily at
                                                                            the number of tasks that go into any process
                                                                            and how they can be smooth, reduced, and
                                                                            effective. This is an especially great model
                                                                            for those with a factory-like output that
                                                                            doesn't vary.</li>
                                                                        <li>Six Sigma Project Management: The Six Sigma
                                                                            method focuses on improving the quality of a
                                                                            project's output. This is especially helpful
                                                                            if you've undergone a lean management style
                                                                            and found the end result less than
                                                                            satisfactory, as Six Sigma highlight on
                                                                            creating a better end result for the
                                                                            customer. This method can be secure onto
                                                                            other management styles, and is a great way
                                                                            to refine.</li>
                                                                        <li>PRINCE2 Project Management: The PRINCE2
                                                                            method is often used by private sectors in
                                                                            the government, and is focused on efficiency
                                                                            and minimising risks and errors. This
                                                                            detail-focused method is all about aiming
                                                                            projects up into product-based steps that
                                                                            can be handled one at a time, ensuring no
                                                                            mistake anywhere in the process.</li>
                                                                    </ul>
