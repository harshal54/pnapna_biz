                                                                    <h5>Project Management Software for Gantt chart</h5>
                                                                    <p class="crm-text"> A Gantt chart can help manage projects by
                                                                        breaking a large project into a series of
                                                                        smaller tasks in an organized way. Each task has
                                                                        particular time duration, presented by a
                                                                        horizontal bar whose left end indicates the
                                                                        beginning date of the task and whose right end
                                                                        indicates the completion date of the task. Tasks
                                                                        may run sequentially, in parallel or
                                                                        overlapping. During the process, significant
                                                                        events can be highlighted as "Milestones,"
                                                                        marked by small diamond shapes.</p>
                                                                    <p class="crm-text">You can check what the sub-tasks are and when
                                                                        each task begins and ends from a Gantt chart.
                                                                        Presenting a visual overview of the project, you
                                                                        can easily track the progress by seeing what
                                                                        should be happening at any stage in the project.
                                                                    </p>
                                                                    <h5 >Project Management Software for PERT Chart</h5>
                                                                    <p class="crm-text"> The PERT stands for "project evaluation and
                                                                        review technique"; it’s a primary project
                                                                        management tool for planning and scheduling
                                                                        entire projects and tracking the implementation
                                                                        phase. PERT charts can also show task division,
                                                                        time allocation, and starting and ending dates.
                                                                        Unlike the Gantt chart, which uses bars to
                                                                        represent tasks, the PERT chart displays
                                                                        information as a network model that uses boxes
                                                                        to represent tasks and arrows to present
                                                                        dependencies between tasks. The layout of a PERT
                                                                        chart makes the relationships between activities
                                                                        easier to see than with Gantt charts. But the
                                                                        disadvantage is it can be challenging when there
                                                                        are many complex dependencies and tasks.</p>
                                                                    <h5>Project Management Software for Calendar</h5>
                                                                    <p class="crm-text">The Calendar is the date or time-based project
                                                                        management tool, which is the easiest to
                                                                        understand. It is more suitable for personal
                                                                        time management better to manage your daily,
                                                                        weekly, or monthly schedules. The outstanding
                                                                        part of this tool is that it has more space for
                                                                        adding to-do lists. It will remind you what to
                                                                        do every day and make sure things can be
                                                                        finished before the deadline.</p>
                                                                    <h5>Project Management Software for Timeline</h5>
                                                                    <p class="crm-text"> The Timeline is also a visual project management
                                                                        tool to help track project progress. With a
                                                                        timeline, you can clearly visualize by what time
                                                                        a task needs to be done. It is a more sequential
                                                                        way of looking at tasks over time. However, the
                                                                        timeline is not as popular as the Gantt chart
                                                                        since it has limitations in showing task
                                                                        dependencies and the completion state.</p>
                                                                    <h5>Project Management Software for WBS Diagram</h5>
                                                                    <p class="crm-text"> The WBS, also known as Work Breakdown Structure,
                                                                        is a common project management tool that helps
                                                                        visualize the scope of work by breaking a
                                                                        project into individual components that can be
                                                                        effectively scheduled. The WBS is a tree-style
                                                                        structure with the overall task on the top,
                                                                        followed by project sections and further into
                                                                        individual tasks. WBS examples are similar to a
                                                                        flowchart that has its components linked
                                                                        logically. The components may be explained in
                                                                        the text or the boxes.</p>
                                                                    <h5>Project Management Software for Mind Map</h5>
                                                                    <p class="crm-text">The mind map is also a powerful tool to manage
                                                                        your projects. Unlike other project management
                                                                        tools, the mind map is less formal, yet more
                                                                        flexible. You can use it to break the whole
                                                                        complex project into smaller tasks that are easy
                                                                        to manage to-do lists and analyze problems. With
                                                                        a mind map, you can insert pictures, link files,
                                                                        and drill down to focus on a specific part,
                                                                        which other tools couldn't do.</p>
                                                                    <h5>Project Management Software for Status Table
                                                                    </h5>
                                                                    <p class="crm-text"> The Status table is an effective tool that is
                                                                        used to track project progress. It doesn't
                                                                        contain too much detail of the project duration
                                                                        and task relations, but with more focus on the
                                                                        status and complete progress. A great function
                                                                        of the status table is that it can include
                                                                        people who take charge of the task, so that
                                                                        project leaders can better evaluate employee
                                                                        performance and know who to reach when there is
                                                                        a problem.</p>
                                                                    <h5>Project Management Software for Mind Map</h5>
                                                                    <p class="crm-text"> HOQ, representing the house of quality, is a
                                                                        quality management tool used to define the
                                                                        relationship between customer desires and
                                                                        product capabilities. It's a tool used in QFD
                                                                        (Quality function deployment) to facilitate
                                                                        group decision-making. Though it looks
                                                                        complicated, it's easy to create through Edraw.
                                                                        All the components in the HOQ, like the roof,
                                                                        matrix, legend box, and markers, are already
                                                                        included in Edraw Max. You can create a
                                                                        high-quality HOQ diagram without much effort.
                                                                    </p>

