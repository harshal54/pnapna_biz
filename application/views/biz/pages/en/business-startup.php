			&nbsp
			<p>The Business Model Canvas (BMC) is a strategic management tool to quickly and easily define and communicate a business idea or concept. The standard framework for a business-model canvas was developed by Alexander Osterwalder and Yves Pigneur in their book Business Model Generation.</p>
			<p>It is a one-page document that works through the fundamental elements of a business or product, structuring an idea in a coherent way. It allows people to get an understanding of your business and to go through the process of making connections between what your idea is and how to make it into a business. Therefore, this tool is very useful to quickly and easily define and communicate a business idea.</p>
			<p>There are nine main building blocks in the business model canvas template:</p>
			<p>1.<span><b>Key Partners:</b></span>  The strategic relationships your business creates with other companies or people</p>
			<p>2.<span><b>Key Activities:</b></span>  Activities or tasks that are integral to operating your company.</p>
			<p>3.<span><b> Key Resources:</b></span> Assets that are required to operate and deliver your company’s value proposition.</p>
			<p>4.<span><b>Value Proposition:</b></span>  The fundamental need that your company is trying to fulfill for its customers. Why your company exists.</p>
			<p>5.<span><b>Customer Relationships:</b></span>  The type of interactions your company has with its customers and the level of support it gives.</p>
			<p>6.<span><b>  Channels:</b></span> Different methods that your company uses to deliver its products and value proposition to customers.</p>
			<p>7.<span><b> Customer Segments:</b></span>  The different groups of customers that your company interacts with.</p>
			<p>8.<span><b> Cost Structure:</b></span>  How a company spends money on operations. The key costs and level of cost focus for your company.</p>
			<p>9.<span><b>  Revenue Streams:</b></span> Your company’s sources of cash flows.</p>
